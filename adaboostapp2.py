    "    symptom_map = {\"无症状\":0, \"头痛\":1, \"癫痫\":2, \"神经功能障碍\":3}\n",
    "    edema_map = {\"无\":0, \"轻度\":1, \"中度\":2, \"重度\":3}\n",
    "    \n",
    "    processed = {\n",
    "        'age': input_dict['age'],\n",
    "        'diameter': input_dict['diameter'],\n",
    "        'SII': input_dict['SII'],\n",
    "        'enhanced': enhanced_map[input_dict['enhanced']],\n",
