    "    input_data['symptom'] = st.selectbox(\"临床症状\", options=[\"无症状\", \"头痛\", \"癫痫\", \"神经功能障碍\"], index=1)\n",
    "    input_data['edema'] = st.selectbox(\"瘤周水肿\", options=[\"无\", \"轻度\", \"中度\", \"重度\"], index=1)\n",
